# Para criarmos uma nova função em Python, utilizamos a palavra-chave def
# seguida do nome da função e um para de parênteses, dentro dos quais listamos
# os eventuais parâmetros
# Em seguida colocamos : para indicar o fim do cabeçalho.
# função + parametros = assinatura da função.
# O inicio do bloco de codigo da função deve começar com identeção
#  de 4 espaços em relação ao cabecalho.

# def <nome da função>([<parâmetros>]):
# <bloco de código>

def soma2(x):
    return x + 2


soma2(6)
